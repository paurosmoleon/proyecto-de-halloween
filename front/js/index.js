function verificarNombreJose() {
    const nombre = document.getElementById("checkJose").value;
    const imagenDiv = document.getElementById("imagenJose");

    if (nombre === "Jose") {
      imagenDiv.style.display = "block";
    } else {
      imagenDiv.style.display = "none"; 
    }
  }
